          padjb(i, j, k) = 0.0
          CALL POPREAL8(v2)
          wadjb(i, j, k, ivx) = wadjb(i, j, k, ivx) + 2*wadj(i, j, k, &
&            ivx)*v2b
          wadjb(i, j, k, ivy) = wadjb(i, j, k, ivy) + 2*wadj(i, j, k, &
&            ivy)*v2b
          wadjb(i, j, k, ivz) = wadjb(i, j, k, ivz) + 2*wadj(i, j, k, &
&            ivz)*v2b
        END DO
      END DO
    END DO
  END IF
  !gammaconstantb = 0.0
