! Use the standard formula.
    DO k=-2,2
      DO j=-2,2
        DO i=-2,2
          CALL PUSHREAL8(v2)
          v2 = wadj(i, j, k, ivx)**2 + wadj(i, j, k, ivy)**2 + wadj(i, j&
&            , k, ivz)**2
        END DO
      END DO
