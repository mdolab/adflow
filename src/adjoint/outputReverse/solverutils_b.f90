            radj(i, j, k) = rj*(one+one/rjk+rij)
            radk(i, j, k) = rk*(one+one/rki+rjk)
          else
            radi(i, j, k) = ri
            radj(i, j, k) = rj
            radk(i, j, k) = rk
          end if
        end do
      case (turkel) 
        call terminate('timestep', &
&                'turkel preconditioner not implemented yet')
      case (choimerkle) 
        call terminate('timestep', &
&                'choi merkle preconditioner not implemented yet')
      end select
    end if
  end subroutine timestep_block
