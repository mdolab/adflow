              flowdomsd(nn, groundlevel, sps)%x(i-1, j, k-1, 2) = &
&               flowdomsd(nn, groundlevel, sps)%x(i-1, j, k-1, 2) + &
&               tempd1
              flowdomsd(nn, groundlevel, sps)%x(i, j, k-1, 2) = &
