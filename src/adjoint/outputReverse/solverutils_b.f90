&               flowdoms(nn, groundlevel, sps)%x(i-1, j, k-1, 3)+&
&               flowdoms(nn, groundlevel, sps)%x(i, j, k, 3))
              call pushreal8array(sc, 3)
              call cellfacevelocities(xc, rotcenter, rotrate, velxgrid, &
