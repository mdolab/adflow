        p1 = vars(zipper%conn(1, i), 1:3)
        p2 = vars(zipper%conn(2, i), 1:3)
        p3 = vars(zipper%conn(3, i), 1:3)
        fx = (p1(1)+p2(1)+p3(1))*triarea
        fy = (p1(2)+p2(2)+p3(2))*triarea
        fz = (p1(3)+p2(3)+p3(3))*triarea
        fp(1) = fp(1) + fx
        fp(2) = fp(2) + fy
        fp(3) = fp(3) + fz
        mx = yc*fz - zc*fy
