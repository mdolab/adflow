      nnx = xx(i+1, j+1, 1) - xx(i, j, 1)
      nnyd = xxd(i+1, j+1, 2) - xxd(i, j, 2)
      nny = xx(i+1, j+1, 2) - xx(i, j, 2)
      nnzd = xxd(i+1, j+1, 3) - xxd(i, j, 3)
      nnz = xx(i+1, j+1, 3) - xx(i, j, 3)
