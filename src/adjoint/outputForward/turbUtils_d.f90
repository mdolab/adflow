          temp9 = w(i+1, j, k, itu1)
          temp8 = si(i-1, j, k, 3)
          temp7 = w(i-1, j, k, itu1)
          temp6 = sj(i, j, k, 3)
          temp5 = w(i, j+1, k, itu1)
          temp4 = sk(i, j, k, 3)
