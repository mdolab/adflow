          temp3 = sj(i, j, k, 1)
          temp4 = w(i, j+1, k, itu1)
          temp5 = sk(i, j, k, 1)
          temp6 = w(i, j, k+1, itu1)
          temp7 = sj(i, j-1, k, 1)
          temp8 = w(i, j-1, k, itu1)
          temp9 = sk(i, j, k-1, 1)
