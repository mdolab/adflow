          temp8 = si(i-1, j, k, 2)
          temp7 = w(i-1, j, k, itu1)
          temp6 = sj(i, j, k, 2)
          temp5 = w(i, j+1, k, itu1)
          temp4 = sk(i, j, k, 2)
          temp3 = w(i, j, k+1, itu1)
          temp2 = sj(i, j-1, k, 2)
          temp1 = w(i, j-1, k, itu1)
          temp0 = sk(i, j, k-1, 2)
          temp = w(i, j, k-1, itu1)
