          end if
          lnwkm1d = abs5d/abs5
          lnwkm1 = log(abs5)
! compute the scaled gradient of ln omega.
          temp10 = si(i, j, k, 1)
          temp9 = si(i-1, j, k, 1)
          temp8 = sj(i, j, k, 1)
          temp7 = sk(i, j, k, 1)
          temp6 = sj(i, j-1, k, 1)
          temp5 = sk(i, j, k-1, 1)
