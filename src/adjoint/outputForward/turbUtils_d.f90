&             )*voli
! compute the normal velocity, where the normal direction
! is taken as the average of faces k and k-1.
          xa = (sk(i, j, k, 1)+sk(i, j, k-1, 1))*voli
          ya = (sk(i, j, k, 2)+sk(i, j, k-1, 2))*voli
          za = (sk(i, j, k, 3)+sk(i, j, k-1, 3))*voli
