&           - temp1*sjd(i, j-1, k, 3) - temp0*wd(i, j, k-1, ivx) - temp*&
&           skd(i, j, k-1, 3)
          uuz = temp9*temp10 - temp7*temp8 + temp5*temp6 + temp3*temp4 -&
&           temp1*temp2 - temp*temp0
! idem for the gradient of v.
          temp10 = si(i, j, k, 1)
          temp9 = w(i+1, j, k, ivy)
          temp8 = si(i-1, j, k, 1)
          temp7 = w(i-1, j, k, ivy)
          temp6 = sj(i, j, k, 1)
          temp5 = w(i, j+1, k, ivy)
          temp4 = sk(i, j, k, 1)
          temp3 = w(i, j, k+1, ivy)
          temp2 = sj(i, j-1, k, 1)
          temp1 = w(i, j-1, k, ivy)
          temp0 = sk(i, j, k-1, 1)
          temp = w(i, j, k-1, ivy)
