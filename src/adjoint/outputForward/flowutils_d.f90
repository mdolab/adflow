&           , k, ivy)+wd(i+1, j+1, k, ivy))
          vbar = fourth*(w(i, j, k, ivy)+w(i+1, j, k, ivy)+w(i, j+1, k, &
&           ivy)+w(i+1, j+1, k, ivy))
