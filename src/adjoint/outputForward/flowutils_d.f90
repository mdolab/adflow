            wzd(i, j, k-1) = wzd(i, j, k-1) + wbard*sz + wbar*szd
            wz(i, j, k-1) = wz(i, j, k-1) + wbar*sz
            qxd(i, j, k-1) = qxd(i, j, k-1) - a2d*sx - a2*sxd
