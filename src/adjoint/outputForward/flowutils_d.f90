&           , k, ivx)+wd(i+1, j+1, k, ivx))
          ubar = fourth*(w(i, j, k, ivx)+w(i+1, j, k, ivx)+w(i, j+1, k, &
&           ivx)+w(i+1, j+1, k, ivx))
