          c1p = ttp*(mulp+muep)*rhoi
          c10d = c1md + c1pd
          c10 = c1m + c1p
! and the omega term.
          muemd = half*(rsstsigwm1d*rev(i-1, j, k)+rsstsigwm1*revd(i-1, &
