   ! pointing.
   ! max(zero, pAdj2(ii,jj)-pAdj1(ii,jj) )
   padj1d(ii, jj) = padj2d(ii, jj) - padj1d(ii, jj)
   padj1(ii, jj) = padj2(ii, jj) - padj1(ii, jj)
   vnd = two*(-(normadj(nnbcs, ii, jj, 1, sps2)*wadj2d(ii, jj, &
   &              ivx))-normadj(nnbcs, ii, jj, 2, sps2)*wadj2d(ii, jj, ivy)-&
   &              normadj(nnbcs, ii, jj, 3, sps2)*wadj2d(ii, jj, ivz))
   vn = two*(rface-wadj2(ii, jj, ivx)*normadj(nnbcs, ii, jj, 1&
   &              , sps2)-wadj2(ii, jj, ivy)*normadj(nnbcs, ii, jj, 2, sps2)&
   &              -wadj2(ii, jj, ivz)*normadj(nnbcs, ii, jj, 3, sps2))
   wadj1d(ii, jj, irho) = wadj2d(ii, jj, irho)
   wadj1(ii, jj, irho) = wadj2(ii, jj, irho)
   wadj1d(ii, jj, ivx) = wadj2d(ii, jj, ivx) + normadj(nnbcs, &
   &              ii, jj, 1, sps2)*vnd
   wadj1(ii, jj, ivx) = wadj2(ii, jj, ivx) + vn*normadj(nnbcs, &
   &              ii, jj, 1, sps2)
   wadj1d(ii, jj, ivy) = wadj2d(ii, jj, ivy) + normadj(nnbcs, &
   &              ii, jj, 2, sps2)*vnd
   wadj1(ii, jj, ivy) = wadj2(ii, jj, ivy) + vn*normadj(nnbcs, &
   &              ii, jj, 2, sps2)
   wadj1d(ii, jj, ivz) = wadj2d(ii, jj, ivz) + normadj(nnbcs, &
   &              ii, jj, 3, sps2)*vnd
   wadj1(ii, jj, ivz) = wadj2(ii, jj, ivz) + vn*normadj(nnbcs, &
   &              ii, jj, 3, sps2)
   ! Just copy the turbulent variables.
   DO l=nt1mg,nt2mg
   wadj1d(ii, jj, l) = wadj2d(ii, jj, l)
   wadj1(ii, jj, l) = wadj2(ii, jj, l)
   END DO
   !
   !        Input the viscous effects - rlv1(), and rev1()
   !
   ! Compute the total energy.
   wadj1d(ii, jj, irhoe) = ovgm1*padj1d(ii, jj) + half*(wadj1d(&
   &              ii, jj, irho)*(wadj1(ii, jj, ivx)**2+wadj1(ii, jj, ivy)**2&
   &              +wadj1(ii, jj, ivz)**2)+wadj1(ii, jj, irho)*(2*wadj1(ii, &
   &              jj, ivx)*wadj1d(ii, jj, ivx)+2*wadj1(ii, jj, ivy)*wadj1d(&
   &              ii, jj, ivy)+2*wadj1(ii, jj, ivz)*wadj1d(ii, jj, ivz)))
   wadj1(ii, jj, irhoe) = ovgm1*padj1(ii, jj) + half*wadj1(ii, &
   &              jj, irho)*(wadj1(ii, jj, ivx)**2+wadj1(ii, jj, ivy)**2+&
   &              wadj1(ii, jj, ivz)**2)
   IF (kpresent) THEN
   wadj1d(ii, jj, irhoe) = wadj1d(ii, jj, irhoe) - factk*(&
   &                wadj1d(ii, jj, irho)*wadj1(ii, jj, itu1)+wadj1(ii, jj, &
   &                irho)*wadj1d(ii, jj, itu1))
   wadj1(ii, jj, irhoe) = wadj1(ii, jj, irhoe) - factk*wadj1(&
   &                ii, jj, irho)*wadj1(ii, jj, itu1)
   END IF
   END DO
   END DO
   ! Extrapolate the state vectors in case a second halo
   ! is needed.
   IF (secondhalo) CALL EXTRAPOLATE2NDHALONKPC_D(nnbcs, icbeg, &
   &                                                icend, jcbeg, jcend, &
   &                                                ioffset, joffset, wadj0&
   &                                                , wadj0d, wadj1, wadj1d&
   &                                                , wadj2, wadj2d, padj0, &
   &                                                padj0d, padj1, padj1d, &
   &                                                padj2, padj2d)
   CALL REPLACEBCSTATESNKPC_D(nnbcs, wadj0, wadj0d, wadj1, wadj1d, &
   &                             wadj2, wadj3, padj0, padj0d, padj1, padj1d&
   &                             , padj2, padj3, rlvadj1, rlvadj2, revadj1, &
   &                             revadj2, icell, jcell, kcell, wadj, wadjd, &
   &                             padj, padjd, rlvadj, revadj, secondhalo, nn&
   &                             , level, sps, sps2)
   END IF
   END IF
   END DO bocos
   END SUBROUTINE BCEULERWALLNKPC_D
