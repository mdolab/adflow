   ! zero at the wall.
   CALL BCEDDYWALL_D(nn)
   ELSE
   ! Any boundary condition but viscous wall. A homogeneous
   ! Neumann condition is applied to the eddy viscosity.
   CALL BCEDDYNOWALL_D(nn)
   END IF
