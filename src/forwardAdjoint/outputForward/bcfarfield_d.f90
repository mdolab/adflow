   &                )*gamma2d(i, j)+gamma2(i, j)*ww2d(i, j, irho)/ww2(i, j, &
   &                irho))
   ELSE IF (ww2(i, j, irho) .EQ. 0.0_8) THEN
   IF (gamma2(i, j) .EQ. 1.0) THEN
   pwr1d = ww2d(i, j, irho)
   ELSE
   pwr1d = 0.0_8
   END IF
   ELSE IF (gamma2(i, j) .EQ. INT(gamma2(i, j))) THEN
   pwr1d = gamma2(i, j)*ww2(i, j, irho)**(gamma2(i, j)-1)*&
   &                ww2d(i, j, irho)
   ELSE
   pwr1d = 0.0_8
   END IF
   pwr1 = ww2(i, j, irho)**gamma2(i, j)
   sfd = (pwr1d*pp2(i, j)-pwr1*pp2d(i, j))/pp2(i, j)**2
   sf = pwr1/pp2(i, j)
   DO l=nt1mg,nt2mg
   ww1d(i, j, l) = ww2d(i, j, l)
   ww1(i, j, l) = ww2(i, j, l)
   END DO
   ELSE
   ! Inflow
   ufd = u0d + (qnfd-qn0d)*nnx + (qnf-qn0)*nnxd
   uf = u0 + (qnf-qn0)*nnx
   vfd = v0d + (qnfd-qn0d)*nny + (qnf-qn0)*nnyd
   vf = v0 + (qnf-qn0)*nny
   wfd = w0d + (qnfd-qn0d)*nnz + (qnf-qn0)*nnzd
   wf = w0 + (qnf-qn0)*nnz
   sfd = s0d
   sf = s0
   DO l=nt1mg,nt2mg
   ww1d(i, j, l) = winfd(l)
   ww1(i, j, l) = winf(l)
   END DO
   END IF
   ! Compute the density, velocity and pressure in the
   ! halo cell.
   ccd = ((cfd*cf+cf*cfd)*gamma2(i, j)-cf**2*gamma2d(i, j))/&
   &            gamma2(i, j)**2
   cc = cf*cf/gamma2(i, j)
   qq = uf*uf + vf*vf + wf*wf
   pwx1d = sfd*cc + sf*ccd
   pwx1 = sf*cc
   IF (pwx1 .GT. 0.0_8) THEN
   ww1d(i, j, irho) = pwx1**ovgm1*(LOG(pwx1)*ovgm1d+ovgm1*pwx1d&
   &              /pwx1)
   ELSE IF (pwx1 .EQ. 0.0_8) THEN
   IF (ovgm1 .EQ. 1.0) THEN
   ww1d(i, j, irho) = pwx1d
   ELSE
   ww1d(i, j, irho) = 0.0_8
   END IF
   ELSE IF (ovgm1 .EQ. INT(ovgm1)) THEN
   ww1d(i, j, irho) = ovgm1*pwx1**(ovgm1-1)*pwx1d
   ELSE
   ww1d(i, j, irho) = 0.0_8
   END IF
   ww1(i, j, irho) = pwx1**ovgm1
   ww1d(i, j, ivx) = ufd
   ww1(i, j, ivx) = uf
   ww1d(i, j, ivy) = vfd
   ww1(i, j, ivy) = vf
   ww1d(i, j, ivz) = wfd
   ww1(i, j, ivz) = wf
   pp1d(i, j) = ww1d(i, j, irho)*cc + ww1(i, j, irho)*ccd
   pp1(i, j) = ww1(i, j, irho)*cc
   ! Simply set the laminar and eddy viscosity to
   ! the value in the donor cell. Their values do
   ! not matter too much in the far field.
   IF (viscous) THEN
   rlv1d(i, j) = rlv2d(i, j)
   rlv1(i, j) = rlv2(i, j)
   END IF
   IF (eddymodel) THEN
   rev1d(i, j) = rev2d(i, j)
   rev1(i, j) = rev2(i, j)
   END IF
   END DO
   END DO
   ! Compute the energy for these halo's.
   CALL COMPUTEETOT_D(icbeg(nn), icend(nn), jcbeg(nn), jcend(nn), &
   &                   kcbeg(nn), kcend(nn), correctfork)
   ! Extrapolate the state vectors in case a second halo
   ! is needed.
   IF (secondhalo) CALL EXTRAPOLATE2NDHALO_D(nn, correctfork)
   END IF
   END DO bocos
   END SUBROUTINE BCFARFIELD_D
