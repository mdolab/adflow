   ac2d = qned - two*(ovgm1d*ce+ovgm1*ced)
   ac2 = qne - two*ovgm1*ce
   ELSE
   ! Inflow or subsonic outflow.
   ac2d = qn0d - two*(ovgm1d*c0+ovgm1*c0d)
   ac2 = qn0 - two*ovgm1*c0
   END IF
