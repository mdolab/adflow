   gamma2d => gammad(2, 1:, 1:)
   gamma2 => gamma(2, 1:, 1:)
   CASE (imax) 
   gamma2d => gammad(il, 1:, 1:)
   gamma2 => gamma(il, 1:, 1:)
   CASE (jmin) 
   gamma2d => gammad(1:, 2, 1:)
   gamma2 => gamma(1:, 2, 1:)
   CASE (jmax) 
   gamma2d => gammad(1:, jl, 1:)
   gamma2 => gamma(1:, jl, 1:)
   CASE (kmin) 
   gamma2d => gammad(1:, 1:, 2)
   gamma2 => gamma(1:, 1:, 2)
   CASE (kmax) 
   gamma2d => gammad(1:, 1:, kl)
   gamma2 => gamma(1:, 1:, kl)
   END SELECT
