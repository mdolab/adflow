   REAL(kind=realtype), DIMENSION(3) :: cfp, cfv, cmp, cmv
   REAL(kind=realtype), DIMENSION(3) :: cfpd, cfvd, cmpd, cmvd
   REAL(kind=realtype) :: yplusmax, scaledim
   REAL(kind=realtype) :: scaledimd
