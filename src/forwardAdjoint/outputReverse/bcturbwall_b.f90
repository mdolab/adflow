   SUBROUTINE BCTURBWALL_B(nn)
   !
   !      ******************************************************************
   !      *                                                                *
   !      * bcTurbWall applies the implicit treatment of the viscous       *
   !      * wall boundary condition for the turbulence model used to the   *
   !      * given subface nn.                                              *
   !      * It is assumed that the pointers in blockPointers are           *
   !      * already set to the correct block.                              *
   !      *                                                                *
   !      ******************************************************************
   !
   USE BLOCKPOINTERS_B
   USE BCTYPES
   USE FLOWVARREFSTATE
   USE INPUTPHYSICS
   USE CONSTANTS
   USE PARAMTURB
   IMPLICIT NONE
   !
   !      Subroutine arguments.
   !
   INTEGER(kind=inttype), INTENT(IN) :: nn
   !
   !      Local variables.
   !
   INTEGER(kind=inttype) :: i, j, ii, jj, iimax, jjmax
   REAL(kind=realtype) :: tmpd, tmpe, tmpf, nu
   REAL(kind=realtype) :: tmpeb, tmpfb, nub
   REAL(kind=realtype), DIMENSION(:, :, :, :), POINTER :: bmt
   REAL(kind=realtype), DIMENSION(:, :, :, :), POINTER :: bmtb
   REAL(kind=realtype), DIMENSION(:, :, :), POINTER :: bvt, ww2
   REAL(kind=realtype), DIMENSION(:, :, :), POINTER :: bvtb, ww2b
   REAL(kind=realtype), DIMENSION(:, :), POINTER :: rlv2, dd2wall
   REAL(kind=realtype), DIMENSION(:, :), POINTER :: rlv2b
   INTRINSIC MIN
   INTRINSIC MAX
   INTRINSIC ABS
   INTEGER :: branch
   REAL(kind=realtype) :: tempb1
   REAL(kind=realtype) :: tempb0
   REAL(kind=realtype) :: abs0b
   REAL(kind=realtype) :: tempb
   REAL(kind=realtype) :: abs0
   INTEGER(kind=inttype) :: y4
   INTEGER(kind=inttype) :: y3
   INTEGER(kind=inttype) :: y2
   INTEGER(kind=inttype) :: y1
   !
   !      ******************************************************************
   !      *                                                                *
   !      * Begin execution                                                *
   !      *                                                                *
   !      ******************************************************************
   !
   ! Set some variables depending on the block face on which the
   ! subface is located. Needed for a general treatment.
   SELECT CASE  (bcfaceid(nn)) 
   CASE (imin) 
   iimax = jl
   jjmax = kl
   bmtb => bmti1b
   bvtb => bvti1b
   ww2b => wb(2, 1:, 1:, :)
   ww2 => w(2, 1:, 1:, :)
   rlv2b => rlvb(2, 1:, 1:)
   rlv2 => rlv(2, 1:, 1:)
   dd2wall => d2wall(2, :, :)
   CASE (imax) 
   iimax = jl
   jjmax = kl
   bmtb => bmti2b
   bvtb => bvti2b
   ww2b => wb(il, 1:, 1:, :)
   ww2 => w(il, 1:, 1:, :)
   rlv2b => rlvb(il, 1:, 1:)
   rlv2 => rlv(il, 1:, 1:)
   dd2wall => d2wall(il, :, :)
   CASE (jmin) 
   iimax = il
   jjmax = kl
   bmtb => bmtj1b
   bvtb => bvtj1b
   ww2b => wb(1:, 2, 1:, :)
   ww2 => w(1:, 2, 1:, :)
   rlv2b => rlvb(1:, 2, 1:)
   rlv2 => rlv(1:, 2, 1:)
   dd2wall => d2wall(:, 2, :)
   CASE (jmax) 
   iimax = il
   jjmax = kl
   bmtb => bmtj2b
   bvtb => bvtj2b
   ww2b => wb(1:, jl, 1:, :)
   ww2 => w(1:, jl, 1:, :)
   rlv2b => rlvb(1:, jl, 1:)
   rlv2 => rlv(1:, jl, 1:)
   dd2wall => d2wall(:, jl, :)
   CASE (kmin) 
   iimax = il
   jjmax = jl
   bmtb => bmtk1b
   bvtb => bvtk1b
   ww2b => wb(1:, 1:, 2, :)
   ww2 => w(1:, 1:, 2, :)
   rlv2b => rlvb(1:, 1:, 2)
   rlv2 => rlv(1:, 1:, 2)
   dd2wall => d2wall(:, :, 2)
   CASE (kmax) 
   iimax = il
   jjmax = jl
   bmtb => bmtk2b
   bvtb => bvtk2b
   ww2b => wb(1:, 1:, kl, :)
   ww2 => w(1:, 1:, kl, :)
   rlv2b => rlvb(1:, 1:, kl)
   rlv2 => rlv(1:, 1:, kl)
   dd2wall => d2wall(:, :, kl)
   END SELECT
   ! Determine the turbulence model used and loop over the faces
   ! of the subface and set the values of bmt and bvt for an
   ! implicit treatment.
   SELECT CASE  (turbmodel) 
   CASE (spalartallmaras, spalartallmarasedwards) 
   ! Spalart-allmaras type of model. Value at the wall is zero,
   ! so simply negate the internal value.
   DO j=bcdata(nn)%jcbeg,bcdata(nn)%jcend
   DO i=bcdata(nn)%icbeg,bcdata(nn)%icend
   CALL PUSHREAL8(bmt(i, j, itu1, itu1))
   bmt(i, j, itu1, itu1) = one
   END DO
   END DO
   DO j=bcdata(nn)%jcend,bcdata(nn)%jcbeg,-1
   DO i=bcdata(nn)%icend,bcdata(nn)%icbeg,-1
   CALL POPREAL8(bmt(i, j, itu1, itu1))
   bmtb(i, j, itu1, itu1) = 0.0_8
   END DO
   END DO
   CASE (komegawilcox, komegamodified, mentersst) 
   !        ================================================================
   ! K-omega type of models. K is zero on the wall and thus the
   ! halo value is the negative of the first internal cell.
   ! For omega the situation is a bit more complicated.
   ! Theoretically omega is infinity, but it is set to a large
   ! value, see menter's paper. The halo value is constructed
   ! such that the wall value is correct. Make sure that i and j
   ! are limited to physical dimensions of the face for the wall
   ! distance. Due to the usage of the dd2Wall pointer and the
   ! fact that the original d2Wall array starts at 2, there is
   ! an offset of -1 present in dd2Wall.
   DO j=bcdata(nn)%jcbeg,bcdata(nn)%jcend
   IF (j .GT. jjmax) THEN
   y1 = jjmax
   ELSE
   y1 = j
   END IF
   IF (2 .LT. y1) THEN
   jj = y1
   ELSE
   jj = 2
   END IF
   DO i=bcdata(nn)%icbeg,bcdata(nn)%icend
   IF (i .GT. iimax) THEN
   y2 = iimax
   ELSE
   y2 = i
   END IF
   IF (2 .LT. y2) THEN
   ii = y2
   ELSE
   ii = 2
   END IF
   nu = rlv2(i, j)/ww2(i, j, irho)
   CALL PUSHREAL8(tmpd)
   tmpd = one/(rkwbeta1*dd2wall(ii-1, jj-1)**2)
   CALL PUSHREAL8(bmt(i, j, itu1, itu1))
   bmt(i, j, itu1, itu1) = one
   CALL PUSHREAL8(bmt(i, j, itu2, itu2))
   bmt(i, j, itu2, itu2) = one
   bvt(i, j, itu2) = two*60.0_realType*nu*tmpd
   END DO
   END DO
   DO j=bcdata(nn)%jcend,bcdata(nn)%jcbeg,-1
   DO i=bcdata(nn)%icend,bcdata(nn)%icbeg,-1
   nub = two*60.0_realType*tmpd*bvtb(i, j, itu2)
   bvtb(i, j, itu2) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu2, itu2))
   bmtb(i, j, itu2, itu2) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu1, itu1))
   bmtb(i, j, itu1, itu1) = 0.0_8
   CALL POPREAL8(tmpd)
   tempb = nub/ww2(i, j, irho)
   rlv2b(i, j) = rlv2b(i, j) + tempb
   ww2b(i, j, irho) = ww2b(i, j, irho) - rlv2(i, j)*tempb/ww2(i, j&
   &         , irho)
   END DO
   END DO
   CASE (ktau) 
   !        ================================================================
   ! K-tau model. Both k and tau are zero at the wall, so the
   ! negative value of the internal cell is taken for the halo.
   DO j=bcdata(nn)%jcbeg,bcdata(nn)%jcend
   DO i=bcdata(nn)%icbeg,bcdata(nn)%icend
   CALL PUSHREAL8(bmt(i, j, itu1, itu1))
   bmt(i, j, itu1, itu1) = one
   CALL PUSHREAL8(bmt(i, j, itu2, itu2))
   bmt(i, j, itu2, itu2) = one
   END DO
   END DO
   DO j=bcdata(nn)%jcend,bcdata(nn)%jcbeg,-1
   DO i=bcdata(nn)%icend,bcdata(nn)%icbeg,-1
   CALL POPREAL8(bmt(i, j, itu2, itu2))
   bmtb(i, j, itu2, itu2) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu1, itu1))
   bmtb(i, j, itu1, itu1) = 0.0_8
   END DO
   END DO
   CASE (v2f) 
   !        ================================================================
   ! V2f turbulence model. Same story for the wall distance as
   ! for k-omega. For this model there is a coupling between the
   ! equations via the boundary conditions.
   DO j=bcdata(nn)%jcbeg,bcdata(nn)%jcend
   IF (j .GT. jjmax) THEN
   y3 = jjmax
   ELSE
   y3 = j
   END IF
   IF (2 .LT. y3) THEN
   jj = y3
   ELSE
   jj = 2
   END IF
   DO i=bcdata(nn)%icbeg,bcdata(nn)%icend
   IF (i .GT. iimax) THEN
   y4 = iimax
   ELSE
   y4 = i
   END IF
   IF (2 .LT. y4) THEN
   ii = y4
   ELSE
   ii = 2
   END IF
   nu = rlv2(i, j)/ww2(i, j, irho)
   CALL PUSHREAL8(tmpd)
   tmpd = one/dd2wall(ii-1, jj-1)**2
   tmpe = two*nu*tmpd
   IF (tmpe*ww2(i, j, itu1) .GE. 0.) THEN
   CALL PUSHREAL8(abs0)
   abs0 = tmpe*ww2(i, j, itu1)
   CALL PUSHCONTROL1B(0)
   ELSE
   CALL PUSHREAL8(abs0)
   abs0 = -(tmpe*ww2(i, j, itu1))
   CALL PUSHCONTROL1B(1)
   END IF
   tmpf = -(20.0_realType*(nu*tmpd)**2/abs0)
   IF (rvfn .EQ. 6) THEN
   tmpf = zero
   CALL PUSHCONTROL1B(0)
   ELSE
   CALL PUSHCONTROL1B(1)
   END IF
   CALL PUSHREAL8(bmt(i, j, itu1, itu1))
   bmt(i, j, itu1, itu1) = one
   CALL PUSHREAL8(bmt(i, j, itu2, itu2))
   bmt(i, j, itu2, itu2) = one
   CALL PUSHREAL8(bmt(i, j, itu3, itu3))
   bmt(i, j, itu3, itu3) = one
   CALL PUSHREAL8(bmt(i, j, itu4, itu4))
   bmt(i, j, itu4, itu4) = one
   CALL PUSHREAL8(bmt(i, j, itu2, itu1))
   bmt(i, j, itu2, itu1) = -(two*tmpe)
   CALL PUSHREAL8(bmt(i, j, itu4, itu3))
   bmt(i, j, itu4, itu3) = -(two*tmpf)
   END DO
   END DO
   DO j=bcdata(nn)%jcend,bcdata(nn)%jcbeg,-1
   DO i=bcdata(nn)%icend,bcdata(nn)%icbeg,-1
   CALL POPREAL8(bmt(i, j, itu4, itu3))
   tmpfb = -(two*bmtb(i, j, itu4, itu3))
   bmtb(i, j, itu4, itu3) = 0.0_8
   nu = rlv2(i, j)/ww2(i, j, irho)
   tmpe = two*nu*tmpd
   CALL POPREAL8(bmt(i, j, itu2, itu1))
   tmpeb = -(two*bmtb(i, j, itu2, itu1))
   bmtb(i, j, itu2, itu1) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu4, itu4))
   bmtb(i, j, itu4, itu4) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu3, itu3))
   bmtb(i, j, itu3, itu3) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu2, itu2))
   bmtb(i, j, itu2, itu2) = 0.0_8
   CALL POPREAL8(bmt(i, j, itu1, itu1))
   bmtb(i, j, itu1, itu1) = 0.0_8
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) tmpfb = 0.0_8
   tempb1 = -(tmpd**2*20.0_realType*tmpfb/abs0)
   nub = 2*nu*tempb1
   abs0b = -(nu**2*tempb1/abs0)
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   CALL POPREAL8(abs0)
   tmpeb = tmpeb + ww2(i, j, itu1)*abs0b
   ww2b(i, j, itu1) = ww2b(i, j, itu1) + tmpe*abs0b
   ELSE
   CALL POPREAL8(abs0)
   tmpeb = tmpeb - ww2(i, j, itu1)*abs0b
   ww2b(i, j, itu1) = ww2b(i, j, itu1) - tmpe*abs0b
   END IF
   nub = nub + two*tmpd*tmpeb
   CALL POPREAL8(tmpd)
   tempb0 = nub/ww2(i, j, irho)
   rlv2b(i, j) = rlv2b(i, j) + tempb0
   ww2b(i, j, irho) = ww2b(i, j, irho) - rlv2(i, j)*tempb0/ww2(i, j&
   &         , irho)
   END DO
   END DO
   END SELECT
   END SUBROUTINE BCTURBWALL_B
