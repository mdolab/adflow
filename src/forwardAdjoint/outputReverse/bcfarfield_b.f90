   !
   INTEGER(kind=inttype) :: nn, i, j, k, l
   REAL(kind=realtype) :: nnx, nny, nnz
   REAL(kind=realtype) :: gm1, ovgm1, ac1, ac2
   REAL(kind=realtype) :: gm1d, ovgm1d, ac1d, ac2d
   REAL(kind=realtype) :: r0, u0, v0, w0, qn0, vn0, c0, s0
   REAL(kind=realtype) :: r0d, u0d, v0d, w0d, qn0d, c0d, s0d
   REAL(kind=realtype) :: re, ue, ve, we, qne, ce
   REAL(kind=realtype) :: red, ued, ved, wed, qned, ced
   REAL(kind=realtype) :: qnf, cf, uf, vf, wf, sf, cc, qq
   REAL(kind=realtype) :: qnfd, cfd, ufd, vfd, wfd, sfd, ccd
   ! Variables Added for forward AD
   REAL(kind=realtype) :: rho, sf2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim, nw) :: ww1, ww2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim, nw) :: ww1d, ww2d
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: pp1, pp2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: pp1d, pp2d
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: gamma1, gamma2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: gamma1d, gamma2d
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: rlv1, rlv2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: rlv1d, rlv2d
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: rev1, rev2
   REAL(kind=realtype), DIMENSION(imaxdim, jmaxdim) :: rev1d, rev2d
   INTRINSIC SQRT
   INTEGER :: branch
   INTEGER :: ad_from
   INTEGER :: ad_to
   INTEGER :: ad_from0
   INTEGER :: ad_to0
   REAL(kind=realtype) :: temp3
   REAL(kind=realtype) :: temp2
   REAL(kind=realtype) :: temp1
   REAL(kind=realtype) :: tempd12
   REAL(kind=realtype) :: temp0
   REAL(kind=realtype) :: tempd11
   REAL(kind=realtype) :: tempd10
   REAL(kind=realtype) :: tempd9
   REAL(kind=realtype) :: tempd
   REAL(kind=realtype) :: tempd8
   REAL(kind=realtype) :: tempd7
   REAL(kind=realtype) :: tempd6
   REAL(kind=realtype) :: tempd5
   REAL(kind=realtype) :: tempd4
   REAL(kind=realtype) :: tempd3
   REAL(kind=realtype) :: tempd2
   REAL(kind=realtype) :: tempd1
   REAL(kind=realtype) :: tempd0
   REAL(kind=realtype) :: temp
   REAL(kind=realtype) :: temp4
   !
   !      ******************************************************************
   !      *                                                                *
   !      * Begin execution                                                *
   !      *                                                                *
   !      ******************************************************************
   !
   ! Some constants needed to compute the riemann inVariants.
   gm1 = gammainf - one
   ovgm1 = one/gm1
   ! Compute the three velocity components, the speed of sound and
   ! the entropy of the free stream.
   r0 = one/winf(irho)
   u0 = winf(ivx)
   v0 = winf(ivy)
   w0 = winf(ivz)
   c0 = SQRT(gammainf*pinfcorr*r0)
   s0 = winf(irho)**gammainf/pinfcorr
   ! Loop over the boundary condition subfaces of this block.
   bocos:DO nn=1,nbocos
   ! Check for farfield boundary conditions.
   IF (bctype(nn) .EQ. farfield) THEN
   ! Nullify the pointers and set them to the correct subface.
   ! They are nullified first, because some compilers require
   ! that.
   !nullify(ww1, ww2, pp1, pp2, rlv1, rlv2, rev1, rev2)
   CALL PUSHREAL8ARRAY(pp2, imaxdim*jmaxdim)
   CALL PUSHREAL8ARRAY(ww2, imaxdim*jmaxdim*nw)
   CALL PUSHREAL8ARRAY(ww1, imaxdim*jmaxdim*nw)
   CALL SETBCPOINTERSBWD(nn, ww1, ww2, pp1, pp2, rlv1, rlv2, rev1&
   &                        , rev2, 0)
   ! Set the additional pointer for gamma2.
   CALL PUSHREAL8ARRAY(gamma2, imaxdim*jmaxdim)
   CALL SETGAMMABWD(nn, gamma1, gamma2)
   ad_from0 = bcdata(nn)%jcbeg
   ! Loop over the generic subface to set the state in the
   ! halo cells.
   DO j=ad_from0,bcdata(nn)%jcend
   ad_from = bcdata(nn)%icbeg
   DO i=ad_from,bcdata(nn)%icend
   ! Store the three components of the unit normal a
   ! bit easier.
   ! replace with actual BCData - Peter Lyu 
   !nnx = BCData(nn)%norm(i,j,1)
   !nny = BCData(nn)%norm(i,j,2)
   !nnz = BCData(nn)%norm(i,j,3)
   ! Compute the normal velocity of the free stream and
   ! substract the normal velocity of the mesh.
   CALL PUSHREAL8(qn0)
   qn0 = u0*bcdata(nn)%norm(i, j, 1) + v0*bcdata(nn)%norm(i, j, 2&
   &           ) + w0*bcdata(nn)%norm(i, j, 3)
   vn0 = qn0 - bcdata(nn)%rface(i, j)
   ! Compute the three velocity components, the normal
   ! velocity and the speed of sound of the current state
   ! in the internal cell.
   re = one/ww2(i, j, irho)
   CALL PUSHREAL8(ue)
   ue = ww2(i, j, ivx)
   CALL PUSHREAL8(ve)
   ve = ww2(i, j, ivy)
   CALL PUSHREAL8(we)
   we = ww2(i, j, ivz)
   CALL PUSHREAL8(qne)
   qne = ue*bcdata(nn)%norm(i, j, 1) + ve*bcdata(nn)%norm(i, j, 2&
   &           ) + we*bcdata(nn)%norm(i, j, 3)
   CALL PUSHREAL8(ce)
   ce = SQRT(gamma2(i, j)*pp2(i, j)*re)
   ! Compute the new values of the riemann inVariants in
   ! the halo cell. Either the value in the internal cell
   ! is taken (positive sign of the corresponding
   ! eigenvalue) or the free stream value is taken
   ! (otherwise).
   IF (vn0 .GT. -c0) THEN
   ! Outflow or subsonic inflow.
   CALL PUSHREAL8(ac1)
   ac1 = qne + two*ovgm1*ce
   CALL PUSHCONTROL1B(0)
   ELSE
   ! Supersonic inflow.
   CALL PUSHREAL8(ac1)
   ac1 = qn0 + two*ovgm1*c0
   CALL PUSHCONTROL1B(1)
   END IF
   IF (vn0 .GT. c0) THEN
   ! Supersonic outflow.
   CALL PUSHREAL8(ac2)
   ac2 = qne - two*ovgm1*ce
   CALL PUSHCONTROL1B(0)
   ELSE
   ! Inflow or subsonic outflow.
   CALL PUSHREAL8(ac2)
   ac2 = qn0 - two*ovgm1*c0
   CALL PUSHCONTROL1B(1)
   END IF
   CALL PUSHREAL8(qnf)
   qnf = half*(ac1+ac2)
   cf = fourth*(ac1-ac2)*gm1
   IF (vn0 .GT. zero) THEN
   ! Outflow.
   uf = ue + (qnf-qne)*bcdata(nn)%norm(i, j, 1)
   vf = ve + (qnf-qne)*bcdata(nn)%norm(i, j, 2)
   wf = we + (qnf-qne)*bcdata(nn)%norm(i, j, 3)
   !Intermediate rho variable added to fix AD bug,ww2
   ! was not getting picked up here. Tapenade 3.6 Does
   ! this properly and this is not required:
   ! rho = ww2(i,j,irho) 
   ! sf = rho**gamma2(i,j)/pp2(i,j) 
   CALL PUSHREAL8(sf)
   sf = ww2(i, j, irho)**gamma2(i, j)/pp2(i, j)
   DO l=nt1mg,nt2mg
   ww1(i, j, l) = ww2(i, j, l)
   END DO
   CALL PUSHCONTROL1B(0)
   ELSE
   ! Inflow
   uf = u0 + (qnf-qn0)*bcdata(nn)%norm(i, j, 1)
   vf = v0 + (qnf-qn0)*bcdata(nn)%norm(i, j, 2)
   wf = w0 + (qnf-qn0)*bcdata(nn)%norm(i, j, 3)
   CALL PUSHREAL8(sf)
   sf = s0
   DO l=nt1mg,nt2mg
   ww1(i, j, l) = winf(l)
   END DO
   CALL PUSHCONTROL1B(1)
   END IF
   ! Compute the density, velocity and pressure in the
   ! halo cell.
   cc = cf*cf/gamma2(i, j)
   ww1(i, j, irho) = (sf*cc)**ovgm1
   ww1(i, j, ivx) = uf
   ww1(i, j, ivy) = vf
   ww1(i, j, ivz) = wf
   pp1(i, j) = ww1(i, j, irho)*cc
   ! Simply set the laminar and eddy viscosity to
   ! the value in the donor cell. Their values do
   ! not matter too much in the far field.
   IF (viscous) THEN
   rlv1(i, j) = rlv2(i, j)
   CALL PUSHCONTROL1B(0)
   ELSE
   CALL PUSHCONTROL1B(1)
   END IF
   IF (eddymodel) THEN
   rev1(i, j) = rev2(i, j)
   CALL PUSHCONTROL1B(1)
   ELSE
   CALL PUSHCONTROL1B(0)
   END IF
   END DO
   CALL PUSHINTEGER4(i - 1)
   CALL PUSHINTEGER4(ad_from)
   END DO
   CALL PUSHINTEGER4(j - 1)
   CALL PUSHINTEGER4(ad_from0)
   CALL RESETGAMMABWD(nn, gamma1, gamma2)
   ! deallocation all pointer
   CALL PUSHREAL8ARRAY(p, SIZE(p, 1)*SIZE(p, 2)*SIZE(p, 3))
   CALL RESETBCPOINTERSBWD(nn, ww1, ww2, pp1, pp2, rlv1, rlv2, &
   &                          rev1, rev2, 0)
   ! Compute the energy for these halo's.
   CALL PUSHREAL8ARRAY(w, SIZE(w, 1)*SIZE(w, 2)*SIZE(w, 3)*SIZE(w, 4)&
   &                  )
   CALL COMPUTEETOT(icbeg(nn), icend(nn), jcbeg(nn), jcend(nn), &
   &                   kcbeg(nn), kcend(nn), correctfork)
   ! Extrapolate the state vectors in case a second halo
   ! is needed.
   IF (secondhalo) THEN
   CALL PUSHREAL8ARRAY(w, SIZE(w, 1)*SIZE(w, 2)*SIZE(w, 3)*SIZE(w, &
   &                     4))
   CALL PUSHREAL8ARRAY(p, SIZE(p, 1)*SIZE(p, 2)*SIZE(p, 3))
   CALL EXTRAPOLATE2NDHALO(nn, correctfork)
   CALL PUSHCONTROL2B(2)
   ELSE
   CALL PUSHCONTROL2B(1)
   END IF
   ELSE
   CALL PUSHCONTROL2B(0)
   END IF
   END DO bocos
   v0d = 0.0_8
   gm1d = 0.0_8
   s0d = 0.0_8
   c0d = 0.0_8
   gamma1d = 0.0_8
   gamma2d = 0.0_8
   w0d = 0.0_8
   u0d = 0.0_8
   ovgm1d = 0.0_8
   DO nn=nbocos,1,-1
   CALL POPCONTROL2B(branch)
   IF (branch .NE. 0) THEN
   IF (branch .NE. 1) THEN
   CALL POPREAL8ARRAY(p, SIZE(p, 1)*SIZE(p, 2)*SIZE(p, 3))
   CALL POPREAL8ARRAY(w, SIZE(w, 1)*SIZE(w, 2)*SIZE(w, 3)*SIZE(w, 4&
   &                    ))
   CALL EXTRAPOLATE2NDHALO_B(nn, correctfork)
   END IF
   CALL POPREAL8ARRAY(w, SIZE(w, 1)*SIZE(w, 2)*SIZE(w, 3)*SIZE(w, 4))
   CALL COMPUTEETOT_B(icbeg(nn), icend(nn), jcbeg(nn), jcend(nn), &
   &                  kcbeg(nn), kcend(nn), correctfork)
   CALL POPREAL8ARRAY(p, SIZE(p, 1)*SIZE(p, 2)*SIZE(p, 3))
   CALL RESETBCPOINTERSBWD_B(nn, ww1, ww1d, ww2, ww2d, pp1, pp1d, pp2&
   &                         , pp2d, rlv1, rlv1d, rlv2, rlv2d, rev1, rev1d&
   &                         , rev2, rev2d, 0)
   CALL RESETGAMMABWD_B(nn, gamma1, gamma1d, gamma2, gamma2d)
   CALL POPINTEGER4(ad_from0)
   CALL POPINTEGER4(ad_to0)
   DO j=ad_to0,ad_from0,-1
   CALL POPINTEGER4(ad_from)
   CALL POPINTEGER4(ad_to)
   DO i=ad_to,ad_from,-1
   CALL POPCONTROL1B(branch)
   IF (branch .NE. 0) THEN
   rev2d(i, j) = rev2d(i, j) + rev1d(i, j)
   rev1d(i, j) = 0.0_8
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   rlv2d(i, j) = rlv2d(i, j) + rlv1d(i, j)
   rlv1d(i, j) = 0.0_8
   END IF
   cf = fourth*(ac1-ac2)*gm1
   cc = cf*cf/gamma2(i, j)
   ww1d(i, j, irho) = ww1d(i, j, irho) + cc*pp1d(i, j)
   wfd = ww1d(i, j, ivz)
   ww1d(i, j, ivz) = 0.0_8
   vfd = ww1d(i, j, ivy)
   ww1d(i, j, ivy) = 0.0_8
   ufd = ww1d(i, j, ivx)
   ww1d(i, j, ivx) = 0.0_8
   temp4 = sf*cc
   IF (temp4 .LE. 0.0_8 .AND. (ovgm1 .EQ. 0.0_8 .OR. ovgm1 .NE. &
   &             INT(ovgm1))) THEN
   tempd11 = 0.0
   ELSE
   tempd11 = ovgm1*temp4**(ovgm1-1)*ww1d(i, j, irho)
   END IF
   ccd = sf*tempd11 + ww1(i, j, irho)*pp1d(i, j)
   pp1d(i, j) = 0.0_8
   sfd = cc*tempd11
   IF (.NOT.temp4 .LE. 0.0_8) ovgm1d = ovgm1d + temp4**ovgm1*LOG(&
   &             temp4)*ww1d(i, j, irho)
   ww1d(i, j, irho) = 0.0_8
   tempd12 = ccd/gamma2(i, j)
   cfd = 2*cf*tempd12
   gamma2d(i, j) = gamma2d(i, j) - cf**2*tempd12/gamma2(i, j)
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   DO l=nt2mg,nt1mg,-1
   ww2d(i, j, l) = ww2d(i, j, l) + ww1d(i, j, l)
   ww1d(i, j, l) = 0.0_8
   END DO
   tempd7 = bcdata(nn)%norm(i, j, 1)*ufd
   tempd6 = bcdata(nn)%norm(i, j, 2)*vfd
   CALL POPREAL8(sf)
   tempd4 = sfd/pp2(i, j)
   temp3 = ww2(i, j, irho)
   temp2 = gamma2(i, j)
   temp1 = temp3**temp2
   IF (.NOT.(temp3 .LE. 0.0_8 .AND. (temp2 .EQ. 0.0_8 .OR. &
   &               temp2 .NE. INT(temp2)))) ww2d(i, j, irho) = ww2d(i, j, &
   &               irho) + temp2*temp3**(temp2-1)*tempd4
   IF (.NOT.temp3 .LE. 0.0_8) gamma2d(i, j) = gamma2d(i, j) + &
   &               temp1*LOG(temp3)*tempd4
   pp2d(i, j) = pp2d(i, j) - temp1*tempd4/pp2(i, j)
   tempd5 = bcdata(nn)%norm(i, j, 3)*wfd
   wed = wfd
   qnfd = tempd6 + tempd7 + tempd5
   qned = -tempd6 - tempd7 - tempd5
   bcdatad(nn)%norm(i, j, 3) = bcdatad(nn)%norm(i, j, 3) + (qnf&
   &             -qne)*wfd
   ved = vfd
   bcdatad(nn)%norm(i, j, 2) = bcdatad(nn)%norm(i, j, 2) + (qnf&
   &             -qne)*vfd
   ued = ufd
   bcdatad(nn)%norm(i, j, 1) = bcdatad(nn)%norm(i, j, 1) + (qnf&
   &             -qne)*ufd
   qn0d = 0.0_8
   ELSE
   DO l=nt2mg,nt1mg,-1
   winfd(l) = winfd(l) + ww1d(i, j, l)
   ww1d(i, j, l) = 0.0_8
   END DO
   tempd10 = bcdata(nn)%norm(i, j, 1)*ufd
   tempd9 = bcdata(nn)%norm(i, j, 2)*vfd
   CALL POPREAL8(sf)
   s0d = s0d + sfd
   tempd8 = bcdata(nn)%norm(i, j, 3)*wfd
   w0d = w0d + wfd
   qnfd = tempd9 + tempd10 + tempd8
   qn0d = -tempd9 - tempd10 - tempd8
   bcdatad(nn)%norm(i, j, 3) = bcdatad(nn)%norm(i, j, 3) + (qnf&
   &             -qn0)*wfd
   v0d = v0d + vfd
   bcdatad(nn)%norm(i, j, 2) = bcdatad(nn)%norm(i, j, 2) + (qnf&
   &             -qn0)*vfd
   u0d = u0d + ufd
   bcdatad(nn)%norm(i, j, 1) = bcdatad(nn)%norm(i, j, 1) + (qnf&
   &             -qn0)*ufd
   ue = ww2(i, j, ivx)
   ve = ww2(i, j, ivy)
   we = ww2(i, j, ivz)
   qned = 0.0_8
   ued = 0.0_8
   ved = 0.0_8
   wed = 0.0_8
   END IF
   tempd3 = fourth*cfd
   ac1d = half*qnfd + gm1*tempd3
   ac2d = half*qnfd - gm1*tempd3
   gm1d = gm1d + (ac1-ac2)*tempd3
   CALL POPREAL8(qnf)
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   CALL POPREAL8(ac2)
   qned = qned + ac2d
   ovgm1d = ovgm1d - two*ce*ac2d
   ced = -(two*ovgm1*ac2d)
   ELSE
   CALL POPREAL8(ac2)
   qn0d = qn0d + ac2d
   ovgm1d = ovgm1d - two*c0*ac2d
   c0d = c0d - two*ovgm1*ac2d
   ced = 0.0_8
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   CALL POPREAL8(ac1)
   qned = qned + ac1d
   ovgm1d = ovgm1d + two*ce*ac1d
   ced = ced + two*ovgm1*ac1d
   ELSE
   CALL POPREAL8(ac1)
   qn0d = qn0d + ac1d
   ovgm1d = ovgm1d + two*c0*ac1d
   c0d = c0d + two*ovgm1*ac1d
   END IF
   re = one/ww2(i, j, irho)
   CALL POPREAL8(ce)
   temp0 = gamma2(i, j)*re
   IF (temp0*pp2(i, j) .EQ. 0.0_8) THEN
   tempd1 = 0.0
   ELSE
   tempd1 = ced/(2.0*SQRT(temp0*pp2(i, j)))
   END IF
   tempd2 = pp2(i, j)*tempd1
   gamma2d(i, j) = gamma2d(i, j) + re*tempd2
   red = gamma2(i, j)*tempd2
   pp2d(i, j) = pp2d(i, j) + temp0*tempd1
   CALL POPREAL8(qne)
   ued = ued + bcdata(nn)%norm(i, j, 1)*qned
   bcdatad(nn)%norm(i, j, 1) = bcdatad(nn)%norm(i, j, 1) + ue*&
   &           qned
   ved = ved + bcdata(nn)%norm(i, j, 2)*qned
   bcdatad(nn)%norm(i, j, 2) = bcdatad(nn)%norm(i, j, 2) + ve*&
   &           qned
   wed = wed + bcdata(nn)%norm(i, j, 3)*qned
   bcdatad(nn)%norm(i, j, 3) = bcdatad(nn)%norm(i, j, 3) + we*&
   &           qned
   CALL POPREAL8(we)
   ww2d(i, j, ivz) = ww2d(i, j, ivz) + wed
   CALL POPREAL8(ve)
   ww2d(i, j, ivy) = ww2d(i, j, ivy) + ved
   CALL POPREAL8(ue)
   ww2d(i, j, ivx) = ww2d(i, j, ivx) + ued
   ww2d(i, j, irho) = ww2d(i, j, irho) - one*red/ww2(i, j, irho)&
   &           **2
   CALL POPREAL8(qn0)
   u0d = u0d + bcdata(nn)%norm(i, j, 1)*qn0d
   bcdatad(nn)%norm(i, j, 1) = bcdatad(nn)%norm(i, j, 1) + u0*&
   &           qn0d
   v0d = v0d + bcdata(nn)%norm(i, j, 2)*qn0d
   bcdatad(nn)%norm(i, j, 2) = bcdatad(nn)%norm(i, j, 2) + v0*&
   &           qn0d
   w0d = w0d + bcdata(nn)%norm(i, j, 3)*qn0d
   bcdatad(nn)%norm(i, j, 3) = bcdatad(nn)%norm(i, j, 3) + w0*&
   &           qn0d
   END DO
   END DO
   CALL POPREAL8ARRAY(gamma2, imaxdim*jmaxdim)
   CALL SETGAMMABWD_B(nn, gamma1, gamma1d, gamma2, gamma2d)
   CALL POPREAL8ARRAY(ww1, imaxdim*jmaxdim*nw)
   CALL POPREAL8ARRAY(ww2, imaxdim*jmaxdim*nw)
   CALL POPREAL8ARRAY(pp2, imaxdim*jmaxdim)
   CALL SETBCPOINTERSBWD_B(nn, ww1, ww1d, ww2, ww2d, pp1, pp1d, pp2, &
   &                       pp2d, rlv1, rlv1d, rlv2, rlv2d, rev1, rev1d, &
   &                       rev2, rev2d, 0)
   END IF
   END DO
   gm1d = gm1d - one*ovgm1d/gm1**2
   IF (gammainf*pinfcorr*r0 .EQ. 0.0_8) THEN
   tempd0 = 0.0
   ELSE
   tempd0 = c0d/(2.0*SQRT(gammainf*pinfcorr*r0))
   END IF
   tempd = s0d/pinfcorr
   temp = winf(irho)**gammainf
   IF (.NOT.(winf(irho) .LE. 0.0_8 .AND. (gammainf .EQ. 0.0_8 .OR. &
   &     gammainf .NE. INT(gammainf)))) winfd(irho) = winfd(irho) + &
   &     gammainf*winf(irho)**(gammainf-1)*tempd
   IF (winf(irho) .LE. 0.0_8) THEN
   gammainfd = gammainfd + r0*pinfcorr*tempd0 + gm1d
   ELSE
   gammainfd = gammainfd + r0*pinfcorr*tempd0 + gm1d + temp*LOG(winf(&
   &     irho))*tempd
   END IF
   pinfcorrd = pinfcorrd + r0*gammainf*tempd0 - temp*tempd/pinfcorr
   r0d = gammainf*pinfcorr*tempd0
   winfd(ivz) = winfd(ivz) + w0d
   winfd(ivy) = winfd(ivy) + v0d
   winfd(ivx) = winfd(ivx) + u0d
   winfd(irho) = winfd(irho) - one*r0d/winf(irho)**2
   END SUBROUTINE BCFARFIELD_B
