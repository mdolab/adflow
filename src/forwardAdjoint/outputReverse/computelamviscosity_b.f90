   SUBROUTINE COMPUTELAMVISCOSITY_B()
   !
   !      ******************************************************************
   !      *                                                                *
   !      * computeLamViscosity computes the laminar viscosity ratio in    *
   !      * the owned cell centers of the given block. Sutherland's law is *
   !      * used. It is assumed that the pointes already point to the      *
   !      * correct block before entering this subroutine.                 *
   !      *                                                                *
   !      ******************************************************************
   !
   USE BLOCKPOINTERS_B
   USE CONSTANTS
   USE FLOWVARREFSTATE
   USE INPUTPHYSICS
   USE ITERATION
   IMPLICIT NONE
   !
   !      Local parameter.
   !
   REAL(kind=realtype), PARAMETER :: twothird=two*third
   !
   !      Local variables.
   !
   INTEGER(kind=inttype) :: i, j, k
   REAL(kind=realtype) :: musuth, tsuth, ssuth, t
   REAL(kind=realtype) :: musuthb, tsuthb, ssuthb, tb
   LOGICAL :: correctfork
   INTEGER :: branch
   REAL(kind=realtype) :: temp2
   REAL(kind=realtype) :: temp1
   REAL(kind=realtype) :: temp0
   REAL(kind=realtype) :: tempb2
   REAL(kind=realtype) :: tempb1
   REAL(kind=realtype) :: tempb0
   REAL(kind=realtype) :: tempb
   REAL(kind=realtype) :: temp
   !
   !      ******************************************************************
   !      *                                                                *
   !      * Begin execution                                                *
   !      *                                                                *
   !      ******************************************************************
   !
   ! Return immediately if no laminar viscosity needs to be computed.
   IF (.NOT.viscous) THEN
   murefb = 0.0_8
   ELSE
   ! Determine whether or not the pressure must be corrected
   ! for the presence of the turbulent kinetic energy.
   IF (kpresent) THEN
   IF (currentlevel .LE. groundlevel .OR. turbcoupled) THEN
   correctfork = .true.
   ELSE
   correctfork = .false.
   END IF
   ELSE
   correctfork = .false.
   END IF
   ! Compute the nonDimensional constants in sutherland's law.
   musuth = musuthdim/muref
   tsuth = tsuthdim/tref
   ssuth = ssuthdim/tref
   ! Substract 2/3 rho k, which is a part of the normal turbulent
   ! stresses, in case the pressure must be corrected.
   IF (correctfork) THEN
   DO k=2,kl
   DO j=2,jl
   DO i=2,il
   p(i, j, k) = p(i, j, k) - twothird*w(i, j, k, irho)*w(i, j, &
   &             k, itu1)
   END DO
   END DO
   END DO
   CALL PUSHCONTROL1B(1)
   ELSE
   CALL PUSHCONTROL1B(0)
   END IF
   ! Add the 2/3 rho k again to the pressure if the pressure was
   ! corrected earlier.
   IF (correctfork) THEN
   DO k=kl,2,-1
   DO j=jl,2,-1
   DO i=il,2,-1
   wb(i, j, k, irho) = wb(i, j, k, irho) + twothird*w(i, j, k, &
   &             itu1)*pb(i, j, k)
   wb(i, j, k, itu1) = wb(i, j, k, itu1) + twothird*w(i, j, k, &
   &             irho)*pb(i, j, k)
   END DO
   END DO
   END DO
   END IF
   ssuthb = 0.0_8
   musuthb = 0.0_8
   tsuthb = 0.0_8
   DO k=kl,2,-1
   DO j=jl,2,-1
   DO i=il,2,-1
   t = p(i, j, k)/(rgas*w(i, j, k, irho))
   temp2 = t/tsuth
   tempb = temp2**1.5_realType*rlvb(i, j, k)/(t+ssuth)
   temp1 = musuth*(tsuth+ssuth)/(t+ssuth)
   tempb0 = -(temp1*tempb)
   tempb1 = 1.5_realType*temp2**0.5*temp1*rlvb(i, j, k)/tsuth
   musuthb = musuthb + (tsuth+ssuth)*tempb
   tsuthb = tsuthb + musuth*tempb - temp2*tempb1
   ssuthb = ssuthb + tempb0 + musuth*tempb
   tb = tempb1 + tempb0
   rlvb(i, j, k) = 0.0_8
   temp0 = w(i, j, k, irho)
   temp = rgas*temp0
   tempb2 = -(p(i, j, k)*tb/temp**2)
   pb(i, j, k) = pb(i, j, k) + tb/temp
   rgasb = rgasb + temp0*tempb2
   wb(i, j, k, irho) = wb(i, j, k, irho) + rgas*tempb2
   END DO
   END DO
   END DO
   CALL POPCONTROL1B(branch)
   IF (branch .NE. 0) THEN
   DO k=kl,2,-1
   DO j=jl,2,-1
   DO i=il,2,-1
   wb(i, j, k, irho) = wb(i, j, k, irho) - twothird*w(i, j, k, &
   &             itu1)*pb(i, j, k)
   wb(i, j, k, itu1) = wb(i, j, k, itu1) - twothird*w(i, j, k, &
   &             irho)*pb(i, j, k)
   END DO
   END DO
   END DO
   END IF
   trefb = trefb - tsuthdim*tsuthb/tref**2 - ssuthdim*ssuthb/tref**2
   murefb = -(musuthdim*musuthb/muref**2)
   END IF
   END SUBROUTINE COMPUTELAMVISCOSITY_B
