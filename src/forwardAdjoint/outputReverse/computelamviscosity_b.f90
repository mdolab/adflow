   SUBROUTINE COMPUTELAMVISCOSITY_B()
   !
   !      ******************************************************************
   !      *                                                                *
   !      * computeLamViscosity computes the laminar viscosity ratio in    *
   !      * the owned cell centers of the given block. Sutherland's law is *
   !      * used. It is assumed that the pointes already point to the      *
   !      * correct block before entering this subroutine.                 *
   !      *                                                                *
   !      ******************************************************************
   !
   USE BLOCKPOINTERS
   USE CONSTANTS
   USE FLOWVARREFSTATE
   USE INPUTPHYSICS
   USE ITERATION
   IMPLICIT NONE
   !
   !      Local parameter.
   !
   REAL(kind=realtype), PARAMETER :: twothird=two*third
   !
   !      Local variables.
   !
   INTEGER(kind=inttype) :: i, j, k
   REAL(kind=realtype) :: musuth, tsuth, ssuth, t
   REAL(kind=realtype) :: musuthd, tsuthd, ssuthd, td
   LOGICAL :: correctfork
   INTEGER :: branch
   REAL(kind=realtype) :: temp2
   REAL(kind=realtype) :: temp1
   REAL(kind=realtype) :: temp0
   REAL(kind=realtype) :: tempd
   REAL(kind=realtype) :: tempd2
   REAL(kind=realtype) :: tempd1
   REAL(kind=realtype) :: tempd0
   REAL(kind=realtype) :: temp
   !
   !      ******************************************************************
   !      *                                                                *
   !      * Begin execution                                                *
   !      *                                                                *
   !      ******************************************************************
   !
   ! Return immediately if no laminar viscosity needs to be computed.
   IF (.NOT.viscous) THEN
   murefd = 0.0_8
   ELSE
   ! Determine whether or not the pressure must be corrected
   ! for the presence of the turbulent kinetic energy.
   IF (kpresent) THEN
   IF (currentlevel .LE. groundlevel .OR. turbcoupled) THEN
   correctfork = .true.
   ELSE
   correctfork = .false.
   END IF
   ELSE
   correctfork = .false.
   END IF
   ! Compute the nonDimensional constants in sutherland's law.
   musuth = musuthdim/muref
   tsuth = tsuthdim/tref
   ssuth = ssuthdim/tref
   ! Substract 2/3 rho k, which is a part of the normal turbulent
   ! stresses, in case the pressure must be corrected.
   IF (correctfork) THEN
   DO k=1,ke
   DO j=1,je
   DO i=1,ie
   p(i, j, k) = p(i, j, k) - twothird*w(i, j, k, irho)*w(i, j, &
   &             k, itu1)
   END DO
   END DO
   END DO
   CALL PUSHCONTROL1B(1)
   ELSE
   CALL PUSHCONTROL1B(0)
   END IF
   ! Add the 2/3 rho k again to the pressure if the pressure was
   ! corrected earlier.
   IF (correctfork) THEN
   DO k=ke,1,-1
   DO j=je,1,-1
   DO i=ie,1,-1
   wd(i, j, k, irho) = wd(i, j, k, irho) + twothird*w(i, j, k, &
   &             itu1)*pd(i, j, k)
   wd(i, j, k, itu1) = wd(i, j, k, itu1) + twothird*w(i, j, k, &
   &             irho)*pd(i, j, k)
   END DO
   END DO
   END DO
   END IF
   ssuthd = 0.0_8
   musuthd = 0.0_8
   tsuthd = 0.0_8
   DO k=ke,1,-1
   DO j=je,1,-1
   DO i=ie,1,-1
   t = p(i, j, k)/(rgas*w(i, j, k, irho))
   temp2 = t/tsuth
   tempd = temp2**1.5_realType*rlvd(i, j, k)/(t+ssuth)
   temp1 = musuth*(tsuth+ssuth)/(t+ssuth)
   tempd0 = -(temp1*tempd)
   tempd1 = 1.5_realType*temp2**0.5*temp1*rlvd(i, j, k)/tsuth
   musuthd = musuthd + (tsuth+ssuth)*tempd
   tsuthd = tsuthd + musuth*tempd - temp2*tempd1
   ssuthd = ssuthd + tempd0 + musuth*tempd
   td = tempd1 + tempd0
   rlvd(i, j, k) = 0.0_8
   temp0 = w(i, j, k, irho)
   temp = rgas*temp0
   tempd2 = -(p(i, j, k)*td/temp**2)
   pd(i, j, k) = pd(i, j, k) + td/temp
   rgasd = rgasd + temp0*tempd2
   wd(i, j, k, irho) = wd(i, j, k, irho) + rgas*tempd2
   END DO
   END DO
   END DO
   CALL POPCONTROL1B(branch)
   IF (branch .NE. 0) THEN
   DO k=ke,1,-1
   DO j=je,1,-1
   DO i=ie,1,-1
   wd(i, j, k, irho) = wd(i, j, k, irho) - twothird*w(i, j, k, &
   &             itu1)*pd(i, j, k)
   wd(i, j, k, itu1) = wd(i, j, k, itu1) - twothird*w(i, j, k, &
   &             irho)*pd(i, j, k)
   END DO
   END DO
   END DO
   END IF
   trefd = trefd - tsuthdim*tsuthd/tref**2 - ssuthdim*ssuthd/tref**2
   murefd = -(musuthdim*musuthd/muref**2)
   END IF
   END SUBROUTINE COMPUTELAMVISCOSITY_B
