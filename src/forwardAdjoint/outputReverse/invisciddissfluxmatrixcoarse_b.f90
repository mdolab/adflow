   wb(i, j, k, ivy) = wb(i, j, k, ivy) - w(i, j, k, irho)*tempb9
   CALL POPREAL8(dru)
   tempb10 = dis0*drub
   wb(i, j+1, k, irho) = wb(i, j+1, k, irho) + w(i, j+1, k, ivx)*&
   &           tempb10
   wb(i, j+1, k, ivx) = wb(i, j+1, k, ivx) + w(i, j+1, k, irho)*&
   &           tempb10
   wb(i, j, k, irho) = wb(i, j, k, irho) - w(i, j, k, ivx)*&
   &           tempb10
   wb(i, j, k, ivx) = wb(i, j, k, ivx) - w(i, j, k, irho)*tempb10
   wb(i, j+1, k, irho) = wb(i, j+1, k, irho) + dis0*drb
   wb(i, j, k, irho) = wb(i, j, k, irho) - dis0*drb
   CALL POPREAL8(dis0)
   END DO
   END DO
   END DO
   DO k=kl,2,-1
   DO j=jl,2,-1
   DO i=il,1,-1
   fsb = fwb(i+1, j, k, irhoe) - fwb(i, j, k, irhoe)
   wavg = half*(w(i+1, j, k, ivz)+w(i, j, k, ivz))
   gammaavg = half*(gamma(i+1, j, k)+gamma(i, j, k))
   gm1 = gammaavg - one
   dr = dis0*(w(i+1, j, k, irho)-w(i, j, k, irho))
   vavg = half*(w(i+1, j, k, ivy)+w(i, j, k, ivy))
   uavg = half*(w(i+1, j, k, ivx)+w(i, j, k, ivx))
   unavg = uavg*sx + vavg*sy + wavg*sz
   abv5 = sx*dru + sy*drv + sz*drw - unavg*dr
   gm53 = gammaavg - five*third
   ovaavg = one/aavg
   ova2avg = one/a2avg
   abv6 = abv3*abv4*ova2avg + abv2*abv5*ovaavg
   abv7 = abv2*abv4*ovaavg + abv3*abv5
   dre = dis0*(w(i+1, j, k, irhoe)-w(i, j, k, irhoe))
   ovgm1 = one/gm1
   alphaavg = half*(uavg**2+vavg**2+wavg**2)
   havg = alphaavg + ovgm1*(a2avg-gm53*kavg)
   lam3b = dre*fsb
   dreb = lam3*fsb
   havgb = abv6*fsb
   abv6b = havg*fsb
   unavgb = abv7*fsb
   abv7b = unavg*fsb
   fsb = fwb(i+1, j, k, imz) - fwb(i, j, k, imz)
   lam3b = lam3b + drw*fsb
   drwb = lam3*fsb
   wavgb = abv6*fsb
   abv6b = abv6b + wavg*fsb
   abv7b = abv7b + sz*fsb
   fsb = fwb(i+1, j, k, imy) - fwb(i, j, k, imy)
   lam3b = lam3b + drv*fsb
   drvb = lam3*fsb
   vavgb = abv6*fsb
   abv6b = abv6b + vavg*fsb
   abv7b = abv7b + sy*fsb
   fsb = fwb(i+1, j, k, imx) - fwb(i, j, k, imx)
   lam3b = lam3b + dru*fsb
   drub = lam3*fsb
   uavgb = abv6*fsb
   abv6b = abv6b + uavg*fsb
   abv7b = abv7b + sx*fsb
   fsb = fwb(i+1, j, k, irho) - fwb(i, j, k, irho)
   abv6b = abv6b + fsb
   abv2b = ovaavg*abv5*abv6b + ovaavg*abv4*abv7b
   abv4b = ova2avg*abv3*abv6b + ovaavg*abv2*abv7b
   ovaavgb = abv2*abv5*abv6b + abv2*abv4*abv7b
   abv3b = ova2avg*abv4*abv6b + abv5*abv7b
   lam3b = lam3b + dr*fsb - abv3b
   abv5b = ovaavg*abv2*abv6b + abv3*abv7b
   ova2avgb = abv3*abv4*abv6b
   unavgb = unavgb - dr*abv5b
   CALL POPREAL8(abv4)
   tempb7 = gm1*abv4b
   drb = alphaavg*tempb7 - unavg*abv5b + lam3*fsb
   drub = drub + sx*abv5b - uavg*tempb7
   drvb = drvb + sy*abv5b - vavg*tempb7
   drwb = drwb + sz*abv5b - wavg*tempb7
   gm1b = (alphaavg*dr-uavg*dru-vavg*drv+dre-wavg*drw)*abv4b
   alphaavgb = dr*tempb7
   uavgb = uavgb - dru*tempb7
   vavgb = vavgb - drv*tempb7
   dreb = dreb + tempb7
   wavgb = wavgb - drw*tempb7
   gm53b = -(drk*abv4b)
   drkb = -(gm53*abv4b)
   CALL POPREAL8(abv3)
   abv1b = abv3b
   CALL POPREAL8(abv2)
   lam1b = half*abv1b + half*abv2b
   lam2b = half*abv1b - half*abv2b
   lam3b = area*lam3b
   lam2b = area*lam2b
   lam1b = area*lam1b
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   rradb = epsshear*lam3b
   lam3b = 0.0_8
   ELSE
   rradb = 0.0_8
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   rradb = rradb + epsacoustic*lam2b
   lam2b = 0.0_8
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   rradb = rradb + epsacoustic*lam1b
   lam1b = 0.0_8
   END IF
   lam3b = lam3b + rradb
   aavgb = rradb
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   CALL POPREAL8(lam3)
   unavgb = unavgb + lam3b
   ELSE
   CALL POPREAL8(lam3)
   unavgb = unavgb - lam3b
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   unavgb = unavgb + lam2b
   aavgb = aavgb - lam2b
   ELSE
   aavgb = aavgb + lam2b
   unavgb = unavgb - lam2b
   END IF
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   unavgb = unavgb + lam1b
   aavgb = aavgb + lam1b
   ELSE
   unavgb = unavgb - lam1b
   aavgb = aavgb - lam1b
   END IF
   alphaavgb = alphaavgb + havgb
   tempb6 = half*alphaavgb
   tempb5 = ovgm1*havgb
   aavgb = aavgb - one*ovaavgb/aavg**2
   IF (a2avg .EQ. 0.0_8) THEN
   a2avgb = tempb5 - one*ova2avgb/a2avg**2
   ELSE
   a2avgb = aavgb/(2.0*SQRT(a2avg)) + tempb5 - one*ova2avgb/&
   &             a2avg**2
   END IF
   uavgb = uavgb + 2*uavg*tempb6 + sx*unavgb
   vavgb = vavgb + 2*vavg*tempb6 + sy*unavgb
   wavgb = wavgb + 2*wavg*tempb6 + sz*unavgb
   CALL POPREAL8(aavg)
   ovgm1b = (a2avg-gm53*kavg)*havgb
   gm53b = gm53b - kavg*tempb5
   kavgb = -(gm53*tempb5)
   gm1b = gm1b - one*ovgm1b/gm1**2
   CALL POPREAL8(area)
   CALL POPREAL8(sz)
   CALL POPREAL8(sy)
   CALL POPREAL8(sx)
   CALL POPREAL8(a2avg)
   temp2 = w(i, j, k, irho)
   temp1 = w(i+1, j, k, irho)
   temp0 = p(i+1, j, k)
   temp = gamma(i+1, j, k)
   tempb3 = half*a2avgb/temp1
   tempb4 = half*a2avgb/temp2
   gammab(i+1, j, k) = gammab(i+1, j, k) + temp0*tempb3
   pb(i+1, j, k) = pb(i+1, j, k) + temp*tempb3
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) - temp*temp0*tempb3/&
   &           temp1
   gammab(i, j, k) = gammab(i, j, k) + p(i, j, k)*tempb4
   pb(i, j, k) = pb(i, j, k) + gamma(i, j, k)*tempb4
   wb(i, j, k, irho) = wb(i, j, k, irho) - gamma(i, j, k)*p(i, j&
   &           , k)*tempb4/temp2
   wb(i+1, j, k, ivz) = wb(i+1, j, k, ivz) + half*wavgb
   wb(i, j, k, ivz) = wb(i, j, k, ivz) + half*wavgb
   wb(i+1, j, k, ivy) = wb(i+1, j, k, ivy) + half*vavgb
   wb(i, j, k, ivy) = wb(i, j, k, ivy) + half*vavgb
   wb(i+1, j, k, ivx) = wb(i+1, j, k, ivx) + half*uavgb
   wb(i, j, k, ivx) = wb(i, j, k, ivx) + half*uavgb
   gammaavgb = gm1b + gm53b
   gammab(i+1, j, k) = gammab(i+1, j, k) + half*gammaavgb
   gammab(i, j, k) = gammab(i, j, k) + half*gammaavgb
   CALL POPCONTROL1B(branch)
   IF (branch .EQ. 0) THEN
   CALL POPREAL8(kavg)
   CALL POPREAL8(drk)
   ELSE
   CALL POPREAL8(kavg)
   wb(i+1, j, k, itu1) = wb(i+1, j, k, itu1) + half*kavgb
   wb(i, j, k, itu1) = wb(i, j, k, itu1) + half*kavgb
   CALL POPREAL8(drk)
   tempb2 = dis0*drkb
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) + w(i+1, j, k, &
   &             itu1)*tempb2
   wb(i+1, j, k, itu1) = wb(i+1, j, k, itu1) + w(i+1, j, k, &
   &             irho)*tempb2
   wb(i, j, k, irho) = wb(i, j, k, irho) - w(i, j, k, itu1)*&
   &             tempb2
   wb(i, j, k, itu1) = wb(i, j, k, itu1) - w(i, j, k, irho)*&
   &             tempb2
   END IF
   wb(i+1, j, k, irhoe) = wb(i+1, j, k, irhoe) + dis0*dreb
   wb(i, j, k, irhoe) = wb(i, j, k, irhoe) - dis0*dreb
   CALL POPREAL8(drw)
   tempb = dis0*drwb
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) + w(i+1, j, k, ivz)*&
   &           tempb
   wb(i+1, j, k, ivz) = wb(i+1, j, k, ivz) + w(i+1, j, k, irho)*&
   &           tempb
   wb(i, j, k, irho) = wb(i, j, k, irho) - w(i, j, k, ivz)*tempb
   wb(i, j, k, ivz) = wb(i, j, k, ivz) - w(i, j, k, irho)*tempb
   CALL POPREAL8(drv)
   tempb0 = dis0*drvb
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) + w(i+1, j, k, ivy)*&
   &           tempb0
   wb(i+1, j, k, ivy) = wb(i+1, j, k, ivy) + w(i+1, j, k, irho)*&
   &           tempb0
   wb(i, j, k, irho) = wb(i, j, k, irho) - w(i, j, k, ivy)*tempb0
   wb(i, j, k, ivy) = wb(i, j, k, ivy) - w(i, j, k, irho)*tempb0
   CALL POPREAL8(dru)
   tempb1 = dis0*drub
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) + w(i+1, j, k, ivx)*&
   &           tempb1
   wb(i+1, j, k, ivx) = wb(i+1, j, k, ivx) + w(i+1, j, k, irho)*&
   &           tempb1
   wb(i, j, k, irho) = wb(i, j, k, irho) - w(i, j, k, ivx)*tempb1
   wb(i, j, k, ivx) = wb(i, j, k, ivx) - w(i, j, k, irho)*tempb1
   wb(i+1, j, k, irho) = wb(i+1, j, k, irho) + dis0*drb
   wb(i, j, k, irho) = wb(i, j, k, irho) - dis0*drb
   CALL POPREAL8(dis0)
   END DO
   END DO
   END DO
   END IF
