   END DO
   END DO
   DO k=2,kl
   DO i=2,il
   tmp14 = w(i, 0, k, irho)*w(i, 0, k, ivx)
   CALL PUSHREAL8(w(i, 0, k, ivx))
   w(i, 0, k, ivx) = tmp14
