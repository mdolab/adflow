            if (branch .eq. 0) then
              ptotd = 0.0_8
              a2d = 0.0_8
              ttotd = 0.0_8
              goto 100
            else
              ww1d(i, j, irho) = ww1d(i, j, irho) + twothird*ww1(i, j, &
&               itu1)*pp1d(i, j)
              ww1d(i, j, itu1) = ww1d(i, j, itu1) + twothird*ww1(i, j, &
&               irho)*pp1d(i, j)
            end if
          else if (branch .ne. 2) then
            ptotd = 0.0_8
            a2d = 0.0_8
            ttotd = 0.0_8
            goto 100
          end if
