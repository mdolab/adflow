          ddd = tempd3
          bbd = -tempd3
          aa2d = -((dd-bb)*tempd3/aa2)
          if (max1 .eq. 0.0_8) then
            max1d = 0.0
          else
            max1d = ddd/(2.0*sqrt(max1))
          end if
          call popcontrol1b(branch)
          if (branch .eq. 0) then
            ddd = max1d
          else
            ddd = 0.0_8
          end if
          bbd = bbd + 2*bb*ddd
          aa2d = aa2d - four*cc*ddd
          ccd = -(four*aa2*ddd)
          betad = half*gm1*2*beta*ccd - gm1*alpha*bbd
