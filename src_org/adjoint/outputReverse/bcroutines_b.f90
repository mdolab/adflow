! 0.0 and 1.0. adapt the velocity and speed of sound
! squared accordingly.
          m2 = q2/a2
          if (one .gt. m2) then
            call pushcontrol1b(0)
            m2 = m2
          else
            m2 = one
            call pushcontrol1b(1)
          end if
