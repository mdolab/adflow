! in the donor cell.
          if (viscous) then
            call pushcontrol1b(0)
          else
            call pushcontrol1b(1)
          end if
          if (eddymodel) then
            rev2d(i, j) = rev2d(i, j) + rev1d(i, j)
            rev1d(i, j) = 0.0_8
          end if
