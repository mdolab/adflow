! pressure, total temperature, total enthalpy, flow
! direction and grid unit outward normal, a bit easier.
          ptot = bcdata(nn)%ptinlet(i, j)
          ttot = bcdata(nn)%ttinlet(i, j)
          htot = bcdata(nn)%htinlet(i, j)
